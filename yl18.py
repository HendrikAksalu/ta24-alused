# Väljasta korduslause abil numbrid 5..1 (for in, range)
# range(start, stop, step)

for x in range(5, 0, -1):
    print(x)

# for – tsükli algus, mis käib läbi kõik väärtused, mis range() annab.
# x – muutuja, mis saab iga tsükliga uue väärtuse.
# range(5, 0, -1) – loob arvujada, mis algab 5-st, lõppeb enne 0-i ja väheneb sammuga -1.